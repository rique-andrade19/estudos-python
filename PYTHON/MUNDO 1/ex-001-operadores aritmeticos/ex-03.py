#fazer uma calculadora de soma simples
n1 = int(input("Digite seu primeiro número: "))
n2 = int(input("Digite seu segundo número: "))
soma = n1 + n2
print("A sua soma é:",soma)
#usando números decimais
n1 = float(input("Seu primeiro número: "))
n2 = float(input("Seu segundo número:"))
soma = n1 + n2
print("Seu resultado é:", soma)
#mostrando na resposta os números digitados também
n1 = int(input("número: "))
n2 = int(input("número 2: "))
soma = n1 + n2
print("A soma entre",n1, "e", n2, "é:", soma)